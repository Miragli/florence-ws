CREATE TABLE appo_user (
    user_id      NUMBER NOT NULL ENABLE,
    user_name    VARCHAR2(100) NOT NULL ENABLE,
    user_surname VARCHAR2(100) NOT NULL ENABLE,
    user_address VARCHAR2(200),
    user_mail    VARCHAR2(100),
    CONSTRAINT "APPO_USER_PK" PRIMARY KEY ( "USER_ID" )
);

CREATE SEQUENCE seq_appo_user MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1;
